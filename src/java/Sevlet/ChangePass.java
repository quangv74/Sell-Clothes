    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession(true);
        AccountUser ac = (AccountUser)session.getAttribute("account");
        String oldpass = request.getParameter("oldpass");
        if(!oldpass.equals(ac.getPassword())){
            request.setAttribute("old", oldpass);
            request.getRequestDispatcher("customer-account3.jsp").forward(request, response);           
        }else{           
            String password = request.getParameter("password1");
            DAOAccount a = new DAOAccount();
            AccountUser au = a.getId(ac.getUsername(),ac.getPassword());
            int n = a.changePass(password,au.getIduser());
            response.getWriter().print(au.getIduser());
            request.setAttribute("changepass", n);
            request.getRequestDispatcher("customer-account3.jsp").forward(request, response);
        }
    }

